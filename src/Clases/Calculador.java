public class Calculador {
    public double suma(double n1,double n2){
          double r = n1 + n2;
        return r;
    } 
    
    public double resta(double n1,double n2){
        double r = n1 - n2;
        return r;
    }
    
    public int multiplicacion(int n1, int n2){
        int r = n1 * n2;
        return r;
    }
    
    public double division(int n1,int n2){
        double r = n1/n2;
        return r;
    }
    
    
}
